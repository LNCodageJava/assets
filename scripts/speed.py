def genGroupsByRank(poke_list, n_groups=5):
    # 1. Trier tous les Pokémon par score décroissant
    sorted_poke = sorted(poke_list, key=lambda x: x[1], reverse=True)

    # 2. Déterminer la taille des groupes
    total = len(sorted_poke)
    base_size = total // n_groups
    remainder = total % n_groups

    groups = []
    start = 0
    for i in range(n_groups):
        # Ajouter 1 si on a un reste à distribuer
        end = start + base_size + (1 if i < remainder else 0)
        group = sorted_poke[start:end]
        # Trier le groupe du plus faible au plus fort
        group.sort(key=lambda x: x[1])
        groups.append(group)
        start = end

    # 3. Afficher
    for i, g in enumerate(groups):
        print(f"--- Groupe {i+1} ---")
        for name, score in g:
            print(f"{name} {score}")
        print()
        
    print("\n// Java sets")
    for i, g in enumerate(groups):
        names = ", ".join(f'"{name}"' for name, _ in g)
        print(f"Set<String> GROUP_{i+1} = Set.of({names});")

